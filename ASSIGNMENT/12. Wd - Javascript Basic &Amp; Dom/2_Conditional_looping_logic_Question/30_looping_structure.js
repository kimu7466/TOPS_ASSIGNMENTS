// Q.30 What are the looping structures in JavaScript? Any one Example?


// ans :

// JavaScript has several looping structures:
// for loop: Executes a block of code a specified number of times.
// while loop: Executes a block of code while a specified condition is true.
// do-while loop: Similar to a while loop, but the block of code is executed at least once before the condition is tested.
// for...in loop: Iterates over the enumerable properties of an object.
// for...of loop: Iterates over the iterable objects like arrays, strings, etc.

// Here's an example of a for loop:


for (let i = 0; i < 5; i++) {
    console.log(i);
}

// This loop will print numbers from 0 to 4 to the console.