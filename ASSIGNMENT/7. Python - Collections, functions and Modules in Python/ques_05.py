# Q.5 How will you compare two lists? 
"""""
The simplest way to compare lists is by using the == operator. It checks if both lists have the same elements in the same order.
"""
list1 = [1, 2, 3]
list2 = [1, 2, 3]

if list1 == list2:
    print("The lists are equal.")
else:
    print("The lists are not equal.")
