# Q.2 :- How to check installed version of django? 

"""
To check the installed version of Django, you can use the following command in your terminal or command prompt:

python -m django --version

Running this command will display the installed version of Django in your Python environment.

If you're using a virtual environment, make sure it is activated before running this command to check the version installed within that virtual environment. If you're not using a virtual environment, the command will display the version of Django installed globally.

"""